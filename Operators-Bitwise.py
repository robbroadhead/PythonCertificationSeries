# Some examples of bitwise operators
# bitwise operators: ~ & ^ | << >>

print("Bitwise Operators Examples")
print("An example of ~ (flip bits)")
print("~4")
print("4 in bits 0100 flip to 1011")
print(format(4,'b'))
print(format(~4,'b'))
print("~5")
print("5 in bits 0101 flip to 1010")
print(format(5,'b'))
print(format(~5,'b'))
a = ~5
print(~a)

print("An example of & (bitwise and)")
print("4 in bits 00000100 5 is 00000101 & turns this into 00000100")
print(format(4 & 5,'b'))
print(format(5 & 4,'b'))
print("17 in bits 00010001 15 is 00001111 & turns this into 00011111")
print(format(17 & 15,'b'))
print(17 & 15)


print("An example of | (bitwise or)")
print("4 in bits 00000100 5 is 00000101 & turns this into 00000101")
print(format(4 | 5,'b'))
print("17 in bits 00010001 15 is 00001111 & turns this into 00011111")
print(format(17 | 15,'b'))
print(17 | 15)

print("An example of ^ (bitwise xor)")
print("4 in bits 00000100 5 is 00000101 ^ turns this into 00000001")
print(format(4 ^ 5,'b'))
print("17 in bits 00010001 15 is 00001111 & turns this into 00011110")
print(format(17 ^ 15,'b'))
print(17 ^ 15)

print("An example of << (bitwise left shift)")
print("4 in bits 00000100 << 2 turns this into 000010000")
print(format(4 << 2,'b'))
print(format(5,'b'))
print(format(5 << 1,'b'))
print(format(5 << 2,'b'))
print(format(5 << 3,'b'))
print(format(5 << 4,'b'))
print("17 in bits 00010001 << 2   turns this into 01000100")
print(format(17,'b'))
print(format(257 << 2,'b'))
print(format(257 << 4,'b'))
print(format(257 << 6,'b'))
print(format(257 << 8,'b'))

print("An example of >> (bitwise right shift)")
print("4 in bits 00000100 >> 2 turns this into 000000001")
print(format(4 >> 2,'b'))
print(format(5,'b'))
print(format(5 >> 1,'b'))
print(format(5 >> 2,'b'))
print(format(5 >> 3,'b'))
print(format(5 >> 4,'b'))
print("17 in bits 00010001 >> 2   turns this into 01000100")
print(format(257,'b'))
print(format(257 >> 2,'b'))
print(format(257 >> 4,'b'))
print(format(257 >> 6,'b'))
print(format(257 >> 8,'b'))

